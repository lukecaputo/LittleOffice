# Define the times for the liturgical hours
def whichHour():
	hour = ""
	now = datetime.datetime.now()

	if 4 <= now.hour < 9:
		hour += "Morning"
	elif 9 <= now.hour < 17:
		hour += "Daytime"
	elif 17 <= now.hour <= 21:
		hour += "Evening"
	else:
		hour += "Night"
	return hour

# Define the days for the readings
def whichDay():
	day = ""
	weekday = datetime.datetime.today().weekday()

	if weekday == 0:
		day += "Monday"
	elif weekday == 1:
		day += "Tuesday"
	elif weekday == 2:
		day += "Wednesday"
	elif weekday == 3:
		day += "Thursday"
	elif weekday == 4:
		day += "Friday"
	elif weekday == 5:
		day += "Saturday"
	else:
		day += "Sunday"
	return day


